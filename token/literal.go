package token

type Literal string

// String implements fmt.Stringer.
func (l Literal) String() string {
	return string(l)
}

func (l Literal) IsReserved() bool {
	if l.IsAtKeyword() {
		return true
	}
	if l.IsKeyword() {
		return true
	}
	if l.IsProperty() {
		return true
	}
	if l.IsElement() {
		return true
	}
	if l.IsPseudoClass() {
		return true
	}
	if l.IsPseudoElement() {
		return true
	}
	if l.IsFunction() {
		return true
	}
	return false
}

func (l Literal) IsKeyword() bool {
	_, ok := Keywords[l]
	return ok
}

func (l Literal) IsAtKeyword() bool {
	_, ok := AtKeywords[l]
	return ok
}

func (l Literal) IsProperty() bool {
	_, ok := Properties[l]
	return ok
}

func (l Literal) IsElement() bool {
	_, ok := Elements[l]
	return ok
}

func (l Literal) IsPseudoClass() bool {
	_, ok := PseudoClasses[l]
	return ok
}

func (l Literal) IsPseudoElement() bool {
	_, ok := PseudoElements[l]
	return ok
}

func (l Literal) IsFunction() bool {
	_, ok := Functions[l]
	return ok
}

